-- =========================================================
-- V42: 무료 인원 반값 과금(2단 요금제) — 청구서에 무료좌석 반값 블록 좌석-일 스냅샷 추가
-- 정책: 활성 > 무료(유료 진입)이면 무료 인원수만큼은 단가의 절반, 초과분(추가인원)은 정단가.
--   공급가 = round( (추가인원 좌석일 × 단가 + 무료좌석 × 유료일수 × 단가/2) ÷ 월일수 )
-- 무료좌석 반값 블록도 유료 진입 익일부터 일할(감원은 당월 유지) — free_block_days = 무료좌석 × 유료일수.
-- =========================================================

ALTER TABLE invoice
    ADD COLUMN IF NOT EXISTS free_block_days BIGINT NOT NULL DEFAULT 0
        COMMENT '무료좌석 반값 블록 좌석-일(무료좌석수 × 유료 상태 일수)' AFTER days_in_month;
